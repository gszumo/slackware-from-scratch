generate_dhcp_systemd () {
#***************************************************
mkdir -pv $SFS/etc/systemd/network/
cat >  $SFS/etc/systemd/network/10-eth0-dhcp.network << "EOF"
[Match]
Name=eth0
[Network]
DHCP=ipv4
EOF
}

generate_screen_clearing () {
#***************************************************
mkdir -pv $SFS/etc/systemd/system/getty@tty1.service.d/
cat > $SFS/etc/systemd/system/getty@tty1.service.d/noclear.conf << EOF
[Service]
TTYVTDisallocate=no
EOF
}

generate_etc_vconsole () {
#***************************************************
mkdir -pv $SFS/etc/
cat > $SFS/etc/vconsole.conf << "EOF"
KEYMAP=fr-latin9
FONT=Lat2-Terminus16
EOF
}

generate_etc_adjtime () {
#***************************************************
mkdir -pv $SFS/etc/
cat > $SFS/etc/adjtime << "EOF"
0.0 0 0.0
0
LOCAL
EOF
}

lists_generator () {
#******************************************
# generator of lists of packages
#******************************************
case $distribution in
	slackware )
		generate_slackware_link_build_list
		generate_slackware_build_list_c
		generate_slackware_build_list2_c
		generate_slackware_build_list3_c
		generate_slackware_build_list4_c ;;

	slackware_14_2 )
		generate_slackware_link_build_list
		generate_slackware_build_list_14_2
		generate_slackware_build_list2_14_2
		generate_slackware_build_list3_14_2
		generate_slackware_build_list4_14_2 ;;

	dlackware )
		generate_dlackware_link_build_list
		generate_dlackware_build_list
		generate_dlackware_build_list2
		generate_dlackware_build_list3
		generate_dhcp_systemd
		generate_screen_clearing
		generate_etc_vconsole
		generate_etc_adjtime ;;

esac
}


